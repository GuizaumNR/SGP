    private void atualizarDataHora() {
        // Atualiza a data
        labelData.setText("Data: " + formato.format(dataSistema));

        // Atualiza a hora
        now = Calendar.getInstance();
        labelHora.setText("Hora: " + String.format("%1$tH:%1$tM:%1$tS", now));
    }
    
    

