package com.gnr.sgp.modelo.dominio;

/**
 * A classe Animais representa informações sobre animais em um sistema. Cada
 * instância desta classe contém detalhes como ID, descrição, quantidade, idade
 * e sexo do animal.
 *
 * @author Guilherme
 */
public class Animais {

    private long id;
    private String descricao;
    private String quantidade;
    private String idade;
    private String sexo;

    public Animais() {
    }

    public Animais(long id, String descricao, String quantidade, String idade, String sexo) {
        this.id = id;
        this.descricao = descricao;
        this.quantidade = quantidade;
        this.idade = idade;
        this.sexo = sexo;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }

    public String getIdade() {
        return idade;
    }

    public void setIdade(String idade) {
        this.idade = idade;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

}
